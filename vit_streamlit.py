# Function to predict disease
def predict_disease(input_image):
    predictions = model(input_image)
    return predictions

# Function to get probable cure from ChatGPT API
def get_probable_remedy(disease_name):
    api_key = 'api_key'  # Replace with your any gemini/opem API key
    headers = {
        'Authorization': f'Bearer {api_key}',
        'Content-Type': 'application/json'
    }
    data = {
        "model": "your_model",
        "messages": [
            {"role": "system", "content": "You are an expert in plant diseases."},
            {"role": "user", "content": f"Provide a probable cure in 20-30 words for {disease_name} in apple plants."}
        ],
        "max_tokens": 50
    }
    response = requests.post('https://api.openai.com/v1/chat/completions', headers=headers, json=data)
    if response.status_code == 200:
        return response.json()['choices'][0]['message']['content']
    else:
        return "Could not retrieve cure information."

